public class Sala {
    private int numero;
    private Aula aula;

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public int getNumero() {
        return this.numero;
    }

    public void setAula(Aula aula) {
        this.aula = aula;
    }

    public Aula getAula() {
        return aula;
    }
}
